package runner

import (
	logger "github.com/Oneledger/protocol/log"
	"os"
)

var log = logger.NewDefaultLogger(os.Stdout).WithPrefix("olvm/interpreter/runner")

func logIfError(msg string, err error) {
	if err != nil {
		log.Error(msg, "err", err)
	}
}
